function generate_figure(name)
fname = ['./figures/' name];
fprintf('*** Generating figure %s and saving it to %s\n', name, fname);
eval(name);
% save EPS
eval(['print2eps ' fname '.eps'])
% save FIG
%saveas(gcf, [fname '.fig']);
close(gcf);
end


